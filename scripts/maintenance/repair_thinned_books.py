#!/usr/bin/env python3
"""Repair book_*.html files damaged by the offset bug in an earlier
thin_books.py run (fixed in the same commit as this script).

The bug: TOC href rewrites (+12 chars each, near the top of the file) were
interleaved with section removals whose offsets were computed on the original
text, so each successive cut landed further backwards — eating the
spl-credit/</nav>/<main>/<h1> region and leaving 12·k-char tail fragments of
removed sections behind. What provably survives intact: the <head>, the nav
TOC (rewrites were string-replacements, applied correctly), and the interiors
of all kept sections (cut windows only ever reached backwards).

Reconstruction per damaged file (detected by a missing <main>):
  head .. </ol>  (verbatim)  +  canonical credit/nav-close/<main>/<h1>
  + each TOC-anchor section, extracted with a balance-aware scanner that
    drops trailing fragments and restores eaten closing tags
  + canonical </main> tail.
"""
import re
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent.parent

SECTION_TOKEN_RE = re.compile(r'<section\b|</section>')
TOP_OPEN_RE = re.compile(r'<section id="([^"]+)">')

CREDIT_NAV_MAIN = (
    '\n    <div class="spl-credit">Generated by '
    '<a href="https://github.com/digital-duck/SPL.py" target="_blank">SPL</a></div>\n'
    '  </nav>\n  <main>\n'
)
TAIL = '\n  </main>\n</div>\n</body>\n</html>\n'


def extract_section(text: str, sec_id: str) -> str | None:
    """Balanced extraction of <section id=X> … its own close tag.

    If the section's trailing close tags were eaten by a mis-cut, extraction
    stops at the next top-level open (or end) and the missing tags are
    appended. Fragment junk after the true close is dropped.
    """
    m = re.search(r'<section id="' + re.escape(sec_id) + r'">', text)
    if not m:
        return None
    next_open = TOP_OPEN_RE.search(text, m.end())
    hard_end = next_open.start() if next_open else len(text)
    depth = 1
    pos = m.end()
    while depth and pos < hard_end:
        t = SECTION_TOKEN_RE.search(text, pos, hard_end)
        if not t:
            break
        depth += 1 if t.group(0) != '</section>' else -1
        pos = t.end()
    if depth == 0:
        return text[m.start():pos]
    # close tags were eaten — keep everything up to the next section, then
    # trim any dangling fragment tail (unopened markup) and re-close.
    body = text[m.start():hard_end].rstrip()
    return body + '</section>' * depth


LI_RE = re.compile(r'<li[^>]*><a href="([^"]+)">[^<]*</a></li>')


def _canonical_li(sec_id: str) -> str:
    if sec_id == 'payoff':
        return '<li class="toc-target"><a href="#payoff">🎯 Payoff</a></li>'
    if sec_id.startswith('phrase_'):
        label = 'Phrase ' + sec_id[len('phrase_'):]
        return f'<li class="toc-target"><a href="#{sec_id}">🌸 {label}</a></li>'
    return f'<li><a href="#{sec_id}">🍃 {sec_id}</a></li>'


def repair(book: Path) -> str | None:
    """Return reconstructed HTML, or None with reason printed if impossible.

    Rebuilds everything from the nav <ol> onward: complete TOC entries are
    kept verbatim; entries clipped by a deep mis-cut (large displacement books
    lost `</ol>` and the tail of the list) are re-derived from the surviving
    top-level sections; then credit/</nav>/<main>/<h1> and the kept sections.
    """
    text = book.read_text(encoding='utf-8')
    nav_pos = text.find('<nav class="toc">')
    ol_pos = text.find('<ol>', nav_pos)
    if nav_pos == -1 or ol_pos == -1:
        return None
    head = text[:ol_pos + len('<ol>')]

    title_m = re.search(r'<title>(.*?)</title>', text, re.S)
    h1 = title_m.group(1).split(' — ', 1)[0].strip() if title_m else book.stem[len('book_'):]

    first_section = TOP_OPEN_RE.search(text)
    toc_region_end = first_section.start() if first_section else len(text)
    lis = [m.group(0) for m in LI_RE.finditer(text, ol_pos, toc_region_end)]
    toc_ids = []
    for li in lis:
        anchor = re.search(r'href="#([^"]+)"', li)
        if anchor:
            toc_ids.append(anchor.group(1))

    # Surviving sections whose TOC entry was clipped off get a canonical one.
    surviving = [m.group(1) for m in TOP_OPEN_RE.finditer(text)]
    for sec_id in surviving:
        if sec_id not in toc_ids:
            lis.append(_canonical_li(sec_id))
            toc_ids.append(sec_id)

    sections = []
    for sec_id in toc_ids:
        sec = extract_section(text, sec_id)
        if sec is None:
            label = sec_id.replace('_', ' ').title()
            sec = f'<section id="{sec_id}">\n<h2>{label}</h2><p>(content not generated)</p>\n</section>'
        sections.append(sec)

    return (
        head + '\n' + '\n'.join(lis) + '\n</ol>'
        + CREDIT_NAV_MAIN
        + f'    <h1 class="book-title">{h1}</h1>\n    '
        + '\n'.join(sections) + TAIL
    )


def needs_repair(text: str) -> bool:
    if '<main>' not in text or '"ref="' in text:
        return True
    # h1 must sit intact between <main> and the first section; a clipped h1
    # or leftover fragment there marks a small-displacement mis-cut.
    if not re.search(r'<main>\s*<h1 class="book-title">[^<]*</h1>', text):
        return True
    first = TOP_OPEN_RE.search(text)
    if first and '</section>' in text[:first.start()]:
        return True
    return False


def main():
    dry_run = '--dry-run' in sys.argv
    repaired = skipped = failed = 0
    for book in sorted(ROOT.glob('public/domains/*/output/*/*/html/book_*.html')):
        text = book.read_text(encoding='utf-8', errors='ignore')
        if not needs_repair(text):
            skipped += 1
            continue
        fixed = repair(book)
        if fixed is None:
            failed += 1
            print(f'  !! cannot repair (no </ol>): {book}')
            continue
        repaired += 1
        if not dry_run:
            book.write_text(fixed, encoding='utf-8')
    mode = 'DRY RUN — no files written' if dry_run else 'done'
    print(f'Repair damaged books ({mode}): {repaired} repaired, '
          f'{skipped} already sound, {failed} failed')


if __name__ == '__main__':
    main()
